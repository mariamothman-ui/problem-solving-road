#include <iostream>
using namespace std ;
int main()
{
    // long long to avoid over flow 
    long long x ; 
    long long y ; 
    cin >> x >> y ;
    cout << x <<" "<<"+"<<" "<<y<<" "<<"="<<" "<<x+y<< endl ;
    cout << x <<" "<<"*"<<" "<<y<<" "<<"="<<" "<<x*y<< endl ;
    cout << x <<" "<<"-"<<" "<<y<<" "<<"="<<" "<<x-y<< endl ;
}
